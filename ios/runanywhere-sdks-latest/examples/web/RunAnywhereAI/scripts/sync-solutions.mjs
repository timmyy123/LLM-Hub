#!/usr/bin/env node
// Sync canonical commons solution YAMLs into a generated TS module.
//
// Web counterpart of examples/ios/RunAnywhereAI/scripts/sync-solutions-yamls.sh
// and examples/react-native/RunAnywhereAI/scripts/sync-solutions-yamls.js:
// reads sdk/runanywhere-commons/examples/solutions/{voice_agent,rag}.yaml and
// emits src/services/solutions-config.ts so the Solutions tab never embeds
// drift-prone inline copies.
//
//   node scripts/sync-solutions.mjs            # regenerate
//   node scripts/sync-solutions.mjs --check    # fail if out of date (CI)
//
// Editing the generated file is forbidden; modify the commons YAMLs instead.
// Dependency-free by design (node built-ins only).

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, '../../../..');
const COMMONS_SOLUTIONS_DIR = path.join(
  REPO_ROOT,
  'sdk/runanywhere-commons/examples/solutions',
);
const OUTPUT_FILE = path.resolve(SCRIPT_DIR, '../src/services/solutions-config.ts');

function readYaml(name) {
  const filePath = path.join(COMMONS_SOLUTIONS_DIR, name);
  if (!fs.existsSync(filePath)) {
    throw new Error(`Commons solution YAML not found: ${filePath}`);
  }
  return fs.readFileSync(filePath, 'utf8');
}

function emitConst(varName, sourceFile, content) {
  // Encode as a JSON string literal so TS sees a plain string regardless of
  // YAML whitespace/escape rules — JSON.stringify handles quotes, newlines,
  // and unicode without any hand-rolled escaping.
  return `// Sourced from sdk/runanywhere-commons/examples/solutions/${sourceFile}\nexport const ${varName} = ${JSON.stringify(content)};\n`;
}

function main() {
  const voiceAgent = readYaml('voice_agent.yaml');
  const rag = readYaml('rag.yaml');

  const banner =
    '// AUTO-GENERATED by examples/web/RunAnywhereAI/scripts/sync-solutions.mjs\n' +
    '// Do not edit by hand. Source of truth: sdk/runanywhere-commons/examples/solutions/*.yaml\n' +
    '// Regenerate via `node scripts/sync-solutions.mjs`.\n\n';
  const body =
    emitConst('VOICE_AGENT_YAML', 'voice_agent.yaml', voiceAgent) +
    '\n' +
    emitConst('RAG_YAML', 'rag.yaml', rag);
  const generated = banner + body;

  if (process.argv.includes('--check')) {
    const existing = fs.existsSync(OUTPUT_FILE)
      ? fs.readFileSync(OUTPUT_FILE, 'utf8')
      : null;
    if (existing !== generated) {
      process.stderr.write(
        `error: ${OUTPUT_FILE} is out of sync with commons solution YAMLs.\n` +
        '       Run: node examples/web/RunAnywhereAI/scripts/sync-solutions.mjs\n',
      );
      process.exit(1);
    }
    process.stdout.write('solutions-config.ts is in sync.\n');
    return;
  }

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, generated, 'utf8');
  process.stdout.write(
    `[sync-solutions] wrote ${path.relative(REPO_ROOT, OUTPUT_FILE)}\n`,
  );
}

main();
